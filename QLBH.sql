------- TAO TABLE --------
CREATE TABLE KHACHHANG(
	MAKH char(4) NOT NULL
	, HOTEN varchar(40)
	, DCHI varchar(50)
	, SODT varchar(20)
	, NGSINH smalldatetime
	, DOANHSO money
	, NGDK smalldatetime
)
CREATE TABLE NHANVIEN(
	MANV char(4) NOT NULL
	, HOTEN varchar(40)
	, SODT varchar(20)
	, NGVL smalldatetime
)
CREATE TABLE SANPHAM(
	MASP char(4) NOT NULL
	, TENSP varchar(40)
	, DVT varchar(20)
	, NUOCSX varchar(40)
	, GIA money
)
CREATE TABLE HOADON(
	SOHD int NOT NULL
	, NGHD smalldatetime
	, MAKH char(4)
	, MANV char(4)
	, TRIGIA money
)
CREATE TABLE CTHD(
	SOHD int NOT NULL
	, MASP char(4) NOT NULL
	, SL int
)

----- TAO PRIMARY KEY ----------
ALTER TABLE KHACHHANG 
