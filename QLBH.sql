FROM HOADON
WHERE TRIGIA = (
    SELECT MAX(TRIGIA)
    FROM HOADON
    WHERE YEAR(NGHD) = 2006
);

-- 8. Tìm họ tên khách hàng đã mua hóa đơn có trị giá cao nhất trong năm 2006.
SELECT KH.HOTEN
FROM KHACHHANG KH
JOIN HOADON HD ON KH.MAKH = HD.MAKH
WHERE HD.TRIGIA = (
    SELECT MAX(TRIGIA)
    FROM HOADON
    WHERE YEAR(NGHD) = 2006
);

-- 9. In ra danh sách 3 khách hàng đầu tiên (MAKH, HOTEN) sắp xếp theo doanh số giảm dần.
SELECT TOP 3 KH.MAKH, KH.HOTEN, SUM(HD.TRIGIA) AS TongDoanhSo
FROM KHACHHANG KH
