ALTER TABLE SANPHAM 
ADD CONSTRAINT PK_SANPHAM PRIMARY KEY(MASP)

ALTER TABLE HOADON 
ADD CONSTRAINT PK_HOADON PRIMARY KEY(SOHD)

ALTER TABLE CTHD 
ADD CONSTRAINT PK_CTHD PRIMARY KEY(SOHD, MASP)

-------- TAO FOREIGN KEY --------------
