SELECT TOP 1 KH.MAKH, KH.HOTEN, COUNT(HD.SOHD) AS SoLanMua
FROM KHACHHANG KH
JOIN HOADON HD ON KH.MAKH = HD.MAKH
GROUP BY KH.MAKH, KH.HOTEN
ORDER BY SoLanMua DESC;

-- 23. Tháng mấy trong năm 2006, doanh số bán hàng cao nhất?
SELECT TOP 1 MONTH(NGHD) AS Thang, SUM(TRIGIA) AS DoanhThu
FROM HOADON
WHERE YEAR(NGHD) = 2006
GROUP BY MONTH(NGHD)
ORDER BY DoanhThu DESC;

-- 24. Tìm sản phẩm (MASP, TENSP) có tổng số lượng bán ra thấp nhất trong năm 2006.
SELECT TOP 1 SP.MASP, SP.TENSP, SUM(CT.SL) AS TongSoLuong
FROM SANPHAM SP
JOIN CTHD CT ON SP.MASP = CT.MASP
JOIN HOADON HD ON CT.SOHD = HD.SOHD
WHERE YEAR(NGHD) = 2006
GROUP BY SP.MASP, SP.TENSP
ORDER BY TongSoLuong ASC;

-- 25. *Mỗi nước sản xuất, tìm sản phẩm (MASP,TENSP) có giá bán cao nhất.
SELECT SP.NUOCSX, SP.MASP, SP.TENSP, SP.GIA
FROM SANPHAM SP
WHERE SP.GIA = (
    SELECT MAX(SP2.GIA)
    FROM SANPHAM SP2
    WHERE SP2.NUOCSX = SP.NUOCSX
);

-- 26. Tìm nước sản xuất sản xuất ít nhất 3 sản phẩm có giá bán khác nhau.
SELECT NUOCSX
FROM SANPHAM
GROUP BY NUOCSX
HAVING COUNT(DISTINCT GIA) >= 3;

-- 27. *Trong 10 khách hàng có doanh số cao nhất, tìm khách hàng có số lần mua hàng nhiều nhất.
WITH Top10Customers AS (
    SELECT TOP 10 KH.MAKH, KH.HOTEN, SUM(HD.TRIGIA) AS TongDoanhSo
    FROM KHACHHANG KH
    JOIN HOADON HD ON KH.MAKH = HD.MAKH
    GROUP BY KH.MAKH, KH.HOTEN
    ORDER BY TongDoanhSo DESC
),
CustomerPurchaseCounts AS (
    SELECT KH.MAKH, KH.HOTEN, COUNT(HD.SOHD) AS SoLanMua
    FROM KHACHHANG KH
    JOIN HOADON HD ON KH.MAKH = HD.MAKH
    WHERE KH.MAKH IN (SELECT MAKH FROM Top10Customers)
    GROUP BY KH.MAKH, KH.HOTEN
)
SELECT TOP 1 MAKH, HOTEN, SoLanMua
FROM CustomerPurchaseCounts
ORDER BY SoLanMua DESC;

